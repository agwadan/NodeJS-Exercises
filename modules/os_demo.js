//Gives us info about our environment

import os from "os";

//Platform
console.log(os.platform());

//CPU Architecture
console.log(os.arch());

//CPU Core information
console.log(os.cpus());

//Free memory
console.log(os.freemem());

//Total memory
console.log(os.totalmem());

//Home Directory
console.log(os.homedir());

//Uptime
console.log(os.uptime());
